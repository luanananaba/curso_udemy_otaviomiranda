# Single quotes.
print('Cuidado ao andar na floresta.')
print('O medo não me deixa sair "daqui".')

# Double quotes.
print("Cuidado quando o sol se pôr.")
print(1, "É melhor que fique 'assim'.")

# Escape.
print(2, "É melhor que fique \"assim\".")

# r
print(r"O que nosso olho não enxerga é o que traz nosso triste \"fim\".")